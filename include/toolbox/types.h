#pragma once
#include <inttypes.h>

// The goal of these types is to have a compact, size-clear
// way to define different common types

typedef signed char        s8;
typedef signed short       s16;
typedef int                s32;
typedef int64_t            s64;

typedef unsigned char      u8;
typedef unsigned short     u16;
typedef unsigned int       u32;
typedef uint64_t           u64;

typedef float              f32;
typedef double             f64;
